CREATE TABLE [dbo].[SYS_RECORD_LIST_SEARCH_CRITERIA] (
    [SRLSC_PRIMARY]     INT           IDENTITY (1, 1) NOT FOR REPLICATION NOT NULL,
    [SRLSC_SEARCH_LINK] INT           NOT NULL,
    [SRLSC_FIELD_NAME]  VARCHAR (64)  NOT NULL,
    [SRLSC_SEARCH_TYPE] CHAR (1)      DEFAULT ('E') NOT NULL,
    [SRLSC_FROM_VALUE]  VARCHAR (256) NULL,
    [SRLSC_TO_VALUE]    VARCHAR (256) NULL,
    CONSTRAINT [SRLSC_PRIMARY_PK] PRIMARY KEY CLUSTERED ([SRLSC_PRIMARY] ASC) WITH (FILLFACTOR = 70),
