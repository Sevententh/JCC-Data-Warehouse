    CONSTRAINT [SRLSC_SEARCH_LINK_FK] FOREIGN KEY ([SRLSC_SEARCH_LINK]) REFERENCES [dbo].[SYS_RECORD_LIST_SEARCHES] ([SRLS_PRIMARY]) ON DELETE CASCADE ON UPDATE CASCADE
);

